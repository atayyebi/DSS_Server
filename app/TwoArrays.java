package util;
	
public class TwoArrays  
{  
	  public float[] a;  
	  public float[] b;  
	  public float Min_a, Max_a;
	  public float Min_b, Max_b;
	  
	  public TwoArrays(float[] a, float[] b, float Min_a, float Max_a, float Min_b, float Max_b)  
	  {  
	    this.a = a; 
	    this.b = b;
	    this.Min_a = Min_a;
	    this.Max_a = Max_a;
	    this.Min_b = Min_b;
	    this.Max_b = Max_b;
	  }  
}
